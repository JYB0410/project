function stripBoilerplate(html) {
  let out = html;
  for (const re of STRIP_PATTERNS) out = out.replace(re, "");
  const seen = new Set();
  out = out.replace(/<p>([\s\S]*?)<\/p>/g, (full, inner) => {
    const key = inner.replace(/<[^>]+>/g, "").trim();
    if (key.length < 40) return full;
    if (seen.has(key)) return "";
    seen.add(key);
    return full;
  });
  return out.replace(/\n{3,}/g, "\n").trim();
}

