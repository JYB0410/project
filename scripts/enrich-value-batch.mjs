function snippet(html) {
  return html.replace(/<[^>]+>/g, "").slice(0, 28);
}

function appendAfterFirstP(content, html) {
  const key = snippet(html);
  if (key && content.includes(key)) return content;
  const idx = content.indexOf("</p>");
  if (idx === -1) return content + html;
  return content.slice(0, idx + 4) + html + content.slice(idx + 4);
}

function appendBeforeLastP(content, html) {
  const key = snippet(html);
  if (key && content.includes(key)) return content;
  const idx = content.lastIndexOf("</p>");
  if (idx === -1) return content + html;
  return content.slice(0, idx) + html + content.slice(idx);
}

